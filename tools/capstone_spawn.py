async def spawn_capstone_instance(
    student_id: str,
    owner_email: str,
    display_name: str,
    student_data: dict,
    venture_slug: str = "breakout",
    custom_domain: Optional[str] = None,
    fan_hub_tenant_id: Optional[str] = None,
    dry_run: bool = False,
) -> CapstoneResult:
    """Spawn Capstone AIOS instance.

    Steps:
      1. Validate input
      2. Generate 5 config files
      3. (dry_run) Save configs to local + return
      4. Insert capstone_instance row (status=spawning)
      5. Spawn Railway service from template repo
      6. Wait for healthcheck
      7. Setup CNAME at Cloudflare
      8. Send magic link email
      9. Update capstone_instance row (status=live)
      10. Return CapstoneResult
    """
    result = CapstoneResult(success=False, student_id=student_id)

    # Step 1: Validate
    student_id = (student_id or "").strip().lower()
    if not student_id or not all(c.isalnum() or c == "-" for c in student_id):
        result.errors.append("invalid student_id (alphanumeric + dash only)")
        return result

    if "@" not in owner_email:
        result.errors.append("invalid owner_email")
        return result

    # Step 2: Generate 5 config files
    student_data["student_id"] = student_id
    student_data["display_name"] = display_name
    student_data["owner_email"] = owner_email
    student_data["venture_slug"] = venture_slug

    configs = {
        "voice_profile.md": generate_voice_profile_md(student_data),
        "style_rules.md": generate_style_rules_md(student_data),
        "story_pool.json": generate_story_pool_json(student_data),
        "compliance.yaml": generate_compliance_yaml(student_data),
        "venture_facts.yaml": generate_venture_facts_yaml(student_data),
    }
    result.config_files = configs

    if dry_run:
        # Save to local for review
        out_dir = f"/tmp/capstone-{student_id}"
        os.makedirs(out_dir, exist_ok=True)
        for filename, content in configs.items():
            with open(os.path.join(out_dir, filename), "w", encoding="utf-8") as f:
                f.write(content)
        result.success = True
        result.subdomain = f"{student_id}.aios.breakout.live"
        result.custom_domain = custom_domain or result.subdomain
        result.next_steps.append(f"Configs saved to {out_dir} for review")
        result.next_steps.append("Anna review configs, then re-run with dry_run=False to actually spawn")
        log.info(f"Dry run: configs saved to {out_dir}")
        return result

    # Step 4: Insert capstone_instance row
    dsn = os.environ.get("DATABASE_URL")
    if not dsn or not asyncpg:
        result.errors.append("DATABASE_URL not set or asyncpg not installed")
        return result

    try:
        conn = await asyncpg.connect(dsn)
    except Exception as exc:
        result.errors.append(f"DB connect fail: {exc}")
        return result

    subdomain = f"{student_id}.aios.breakout.live"
    primary_domain = custom_domain or subdomain
    magic_link_token = str(uuid.uuid4())

    try:
        await conn.execute("""
            INSERT INTO capstone_instance (
                student_id, subdomain, custom_domain, fan_hub_subdomain, fan_hub_tenant_id,
                config_voice_profile, config_style_rules, config_story_pool, config_compliance,
                config_venture_facts, status, admin_magic_link_token, kernel_version
            )
            VALUES ($1, $2, $3, $4, $5::uuid, $6, $7, $8::jsonb, $9, $10, 'spawning', $11::uuid, 'v3.0.0')
            ON CONFLICT (student_id) DO UPDATE SET
                config_voice_profile = EXCLUDED.config_voice_profile,
                config_style_rules = EXCLUDED.config_style_rules,
                config_story_pool = EXCLUDED.config_story_pool,
                config_compliance = EXCLUDED.config_compliance,
                config_venture_facts = EXCLUDED.config_venture_facts,
                status = 'spawning',
                updated_at = NOW()
        """, student_id, subdomain, custom_domain,
            f"{student_id}.fan.breakout.live",
            fan_hub_tenant_id,
            configs["voice_profile.md"], configs["style_rules.md"],
            configs["story_pool.json"], configs["compliance.yaml"],
            configs["venture_facts.yaml"],
            magic_link_token)

        result.subdomain = subdomain
        result.custom_domain = primary_domain

    finally:
        await conn.close()

    # Step 5: Spawn Railway service
    railway_service = await _spawn_railway_service(student_id, configs)
    if railway_service.get("error"):
        result.errors.append(f"Railway spawn fail: {railway_service['error']}")
        result.next_steps.append("Manual fallback: Anna fork repo + deploy via Railway dashboard")
        return result

    result.railway_service_id = railway_service.get("service_id")
    result.railway_service_name = railway_service.get("service_name")

    # Step 6: Wait health (skipped in skeleton - manual verify)
    result.next_steps.append("Wait 3-5 min for Railway service health, verify at admin URL")

    # Step 7: CNAME
    if not custom_domain:
        cname_ok = await _create_aios_cname(student_id, railway_service.get("railway_url", ""))
        result.cname_created = cname_ok
        if not cname_ok:
            result.errors.append("CNAME failed - Anna setup manual in Cloudflare dashboard")
            result.next_steps.append(f"Add CNAME `{student_id}.aios` → `{railway_service.get('railway_url')}` proxied=False")

    # Step 8: Send email
    result.admin_url = f"https://{primary_domain}/admin?token={magic_link_token}"
    email_ok = await _send_capstone_email(owner_email, display_name, result.admin_url, primary_domain)
    result.email_sent = email_ok
    if not email_ok:
        result.errors.append("Email send failed - Anna gửi manual")
        result.next_steps.append(f"Anna copy admin URL gửi cho student: {result.admin_url}")

    result.success = len(result.errors) == 0
    return result


