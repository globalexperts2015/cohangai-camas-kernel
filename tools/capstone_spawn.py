def generate_story_pool_json(student_data: dict) -> str:
    """Generate story_pool.json from stories collected during Content Engine."""
    story_pool = student_data.get("story_pool", [])

    structured_stories = []
    for i, story in enumerate(story_pool, 1):
        if isinstance(story, str):
            structured_stories.append({
                "id": f"S{i:02d}",
                "raw": story,
                "tags": [],
                "usable_for": ["reel", "fb_post", "email"],
                "public_safe": True
            })
        elif isinstance(story, dict):
            structured_stories.append({
                "id": story.get("id", f"S{i:02d}"),
                "raw": story.get("raw", ""),
                "tags": story.get("tags", []),
                "usable_for": story.get("usable_for", ["reel", "fb_post", "email"]),
                "public_safe": story.get("public_safe", True)
            })

    return json.dumps({
        "version": "1.0",
        "generated_at": datetime.utcnow().isoformat(),
        "student_id": student_data.get("student_id", ""),
        "story_count": len(structured_stories),
        "stories": structured_stories
    }, ensure_ascii=False, indent=2)


